    private void reset_btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reset_btn2ActionPerformed
        // TODO add your handling code here:
        if(!isTextFieldEmpty() && checkThresholdLimit())
        {
            var_lower_low = Integer.parseInt(jTextField_LowImpact_low.getText());
            var_lower_up = Integer.parseInt(jTextField_LowImpact_up.getText());
            var_med_low = Integer.parseInt(jTextField_MedImpact_low.getText());
            var_med_up = Integer.parseInt(jTextField_MedImpact_up.getText());
            var_high_low = Integer.parseInt(jTextField_HighImpact_low.getText());
            method_lower_low = Integer.parseInt(jTextField_LowImpact_low1.getText());
            method_lower_up = Integer.parseInt(jTextField_LowImpact_up1.getText());
            method_med_low = Integer.parseInt(jTextField_MedImpact_low1.getText());
            method_med_up = Integer.parseInt(jTextField_MedImpact_up1.getText());
            method_high_low = Integer.parseInt(jTextField_HighImpact_low1.getText());
            this.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(this, "Fill All fields.\nOR\nCheck Threshold Limit.","Information",JOptionPane.INFORMATION_MESSAGE);
            this.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        }
    }//GEN-LAST:event_reset_btn2ActionPerformed

