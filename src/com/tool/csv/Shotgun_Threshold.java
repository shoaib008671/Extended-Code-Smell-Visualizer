    private boolean isTextFieldEmpty()
    {
        if(jTextField_LowImpact_low.getText().isEmpty() || jTextField_LowImpact_up.getText().isEmpty()
                || jTextField_MedImpact_low.getText().isEmpty() || jTextField_MedImpact_up.getText().isEmpty()
                || jTextField_HighImpact_low.getText().isEmpty() || jTextField_HighImpact_up.getText().isEmpty()
                || jTextField_LowImpact_low1.getText().isEmpty() || jTextField_LowImpact_up1.getText().isEmpty()
                || jTextField_MedImpact_low1.getText().isEmpty() || jTextField_MedImpact_up1.getText().isEmpty()
                || jTextField_HighImpact_low1.getText().isEmpty() || jTextField_HighImpact_up1.getText().isEmpty())
        {
            return true;
        }
        else
            return false;
    }
    
    private boolean checkThresholdLimit()
    {
        if(!isTextFieldEmpty())
        {
            if((Integer.parseInt(jTextField_LowImpact_low.getText()) > Integer.parseInt(jTextField_LowImpact_up.getText()))
                    || (Integer.parseInt(jTextField_MedImpact_low.getText()) > Integer.parseInt(jTextField_MedImpact_up.getText()))
                    || (Integer.parseInt(jTextField_LowImpact_low1.getText()) > Integer.parseInt(jTextField_LowImpact_up1.getText()))
                    || (Integer.parseInt(jTextField_MedImpact_low1.getText()) > Integer.parseInt(jTextField_MedImpact_up1.getText())))
            {
                return false;
            }
            else
            {
                return true;
            }
                    
        }
        else{
            return false;
        }
    }
